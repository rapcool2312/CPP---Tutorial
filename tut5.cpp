#include<iostream>
using namespace std;

int main()
{
    int num1,num2;
    cout<<"Enter the value of num1: "; /* '<<' This is an Insertion Operator */
    cin>>num1; /* '>>' This is an Extraction Operator */

    cout<<"\nEnter the value of num2: "; /* '<<' This is an Insertion Operator */
    cin>>num2; /* '>>' This is an Extraction Operator */

    cout<<"\nThe sum of num1 & num2: "<<num1+num2;
    return 0;
}
